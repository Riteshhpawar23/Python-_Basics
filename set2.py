#-----------------------------add and update
s={1,2,3,1,2,3,4,5,4,5,6,}

print(s)

s.add(34) # .add will add element to set

print(s)

s.add('ritesh') #this will add whole word to set

s.update("ritesh") #.update this will add charater to set

print(s)

#--------------------------- remove and pop

s.pop()         # pop will remove random element from the set


print(s)

s.remove("r")          # remove will remove specific element from the set

print(s)